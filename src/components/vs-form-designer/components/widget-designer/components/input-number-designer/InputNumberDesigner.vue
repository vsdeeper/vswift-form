<script setup lang="ts">
import { QuestionFilled } from '@element-plus/icons-vue'
import type { DInputNumberOptions } from '../../../form-design-area'
import { CONTROLS_POSITION_OPTIONS } from '../constants'
import type { WidgetDesignData } from '@/components/vs-form-designer'
import { getFormDesignData } from '@/stores'

const model = defineModel<WidgetDesignData<DInputNumberOptions>>({ default: () => ({}) })

const showColumnWidth = (id: string) => {
  const formDesignData = getFormDesignData()
  const filterTable = formDesignData?.widgetList.filter(e => e.type === 'data-table')
  return filterTable?.some(e => e.widgetList?.some(a => a.id === id))
}
</script>

<template>
  <el-form :model ref="formRef" label-width="100px">
    <el-form-item label="唯一标识" prop="id" :rules="[{ required: true, message: '必填项' }]">
      <el-input :model-value="model.id" placeholder="请输入" disabled />
    </el-form-item>
    <el-form-item prop="idAlias">
      <template #label>
        ID别名
        <el-tooltip
          content="表单的 value 值对应的 key，默认为唯一标识"
          placement="top"
          effect="dark"
        >
          <el-icon><QuestionFilled /></el-icon>
        </el-tooltip>
      </template>
      <el-input v-model="model.idAlias" placeholder="请输入" />
    </el-form-item>
    <el-form-item label="标签名称" prop="options.label">
      <el-input v-model="model.options.label" placeholder="请输入" />
    </el-form-item>
    <el-form-item label="占位内容" prop="options.placeholder">
      <el-input v-model="model.options.placeholder" placeholder="请输入" type="textarea" autosize />
    </el-form-item>
    <el-form-item label="使用控制按钮" prop="options.controls">
      <el-switch v-model="model.options.controls" :active-value="true" :inactive-value="false" />
    </el-form-item>
    <el-form-item
      v-if="model.options.controls"
      label="控制按钮位置"
      prop="options.controlsPosition"
    >
      <el-select v-model="model.options.controlsPosition" placeholder="请选择" clearable filterable>
        <el-option
          v-for="item in CONTROLS_POSITION_OPTIONS"
          :key="item.value"
          :label="item.label"
          :value="item.value"
        />
      </el-select>
    </el-form-item>
    <el-form-item label="默认值" prop="options.defaultValue">
      <el-input-number
        v-model="model.options.defaultValue"
        placeholder="请输入"
        controls-position="right"
        :max="Number.MAX_SAFE_INTEGER"
      />

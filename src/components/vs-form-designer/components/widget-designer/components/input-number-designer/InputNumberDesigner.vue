        placeholder="请输入"
        controls-position="right"
        :max="Number.MAX_SAFE_INTEGER"
      />
    </el-form-item>
    <el-form-item label="最大值" prop="options.max">
      <el-input-number
        v-model="model.options.max"
        placeholder="请输入"
        controls-position="right"
        :max="Number.MAX_SAFE_INTEGER"
      />
    </el-form-item>
    <el-form-item label="数值精度" prop="options.precision">
      <el-input-number
        v-model="model.options.precision"
        placeholder="请输入"
        controls-position="right"
